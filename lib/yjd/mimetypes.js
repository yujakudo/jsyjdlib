/**
 * Get MIME type from extension.
 * @param {string} s_fn The extension, file name, or path.
 * @return {string} MIME type.
 */
yjd.mimeTypes.getFromExt = function(s_fn) {
	var pos = s_fn.lastIndexOf('/');
	if(pos>=0) s_fn = s_fn.substr(pos+1);
	pos = s_fn.lastIndexOf('\\');
	if(pos>=0) s_fn = s_fn.substr(pos+1);
	pos = s_fn.lastIndexOf('.');
	if(pos>=0) s_fn = s_fn.substr(pos+1);
	s_fn = s_fn.toLowerCase();
	if(yjd.mimeTypes.list.hasOwnProperty(s_fn)) {
		return yjd.mimeTypes.list[s_fn];
	}
	return yjd.mimeTypes.list.default;
}
